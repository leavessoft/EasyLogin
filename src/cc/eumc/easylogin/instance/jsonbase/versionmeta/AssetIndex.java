package cc.eumc.easylogin.instance.jsonbase.versionmeta;

// ----------------------------------
public class AssetIndex {
    String id;
    String sha1;
    int size;
    int totalSize;
    String url;

    public String getId() {
        return id;
    }

    public String getSha1() {
        return sha1;
    }

    public int getSize() {
        return size;
    }

    public int getTotalSize() {
        return totalSize;
    }

    public String getUrl() {
        return url;
    }
}
